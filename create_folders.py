import os

def create_folders(folder_names):
  """
  주어진 이름들로 폴더를 생성합니다.

  Args:
    folder_names: 생성할 폴더 이름들의 리스트.
  """
  for name in folder_names:
    try:
      os.makefiles(name+".html")
      print(f"'{name}' 폴더를 성공적으로 만들었습니다.")
    except FileExistsError:
      print(f"'{name}' 폴더가 이미 존재합니다.")
    except OSError as e:
      print(f"'{name}' 폴더를 만드는 중 오류가 발생했습니다: {e}")

